package ar.edu.untref.cdm.domain;

public class Career {

	private String name;
	private StudyPlan studyPlan;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public StudyPlan getStudyPlan() {
		return studyPlan;
	}

	public void setStudyPlans(StudyPlan studyPlan) {
		this.studyPlan = studyPlan;
	}

}